import java.io.IOException;
public class AppXXX1 {
    public static void main(String[] args) throws IOException {
        while (true) {
            int ch = System.in.read();
            System.out.print((char) ch + " ");
            System.out.print((char) ch + "  ");
            System.out.print((char) ch + "   ");
            System.out.print((char) ch + "    ");
            System.out.println((char) ch);
        }
    }
}
